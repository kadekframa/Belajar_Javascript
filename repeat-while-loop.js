// While loop pada JavaScript.

// while loop adalah versi perulangan yang lebih sederhana dibanding dengan for loop.
// di while loop, hanya terdapat kondisi perulangan, tanpa ada init statement dan post statement.


// Contoh penggunaan while loop.
let counter = 1;
while(counter <= 10){
    document.writeln(`<p>Perulangan ke ${counter}</p>`);
    counter++
}



// NB: Apabila dalam melakukan perulangan kita hanya membutuhkan kondisi nya saja, maka disarankan kita tidak perlu menggunakan for loop namun dapat menggunakan while loop.
